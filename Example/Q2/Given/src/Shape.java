
public class Shape {

    private double perimeter;
    private double area;

    public Shape() {
    }

    public double getPerimeter() {
        return perimeter;
    }

    public void setPerimeter(double value) {
        this.perimeter = value;
    }

    public double getArea() {
        return area;
    }

    public void setArea(double value) {
        this.area = value;
    }
}
